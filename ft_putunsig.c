#include "libftprintf.h"

void	ft_putunsig(unsigned int n)
{
	if (n < 10)
		ft_putchar(n + 48);
	else
	{
		ft_putunsig(n / 10);
		ft_putchar((char)(n % 10 + 48));
	}
}
