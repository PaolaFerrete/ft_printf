#include "libftprintf.h"

void	ft_puthex(unsigned int h)
{
	char	*hex;

	hex = "0123456789abcdef";
	if (h >= 16)
	{
		ft_puthex(h / 16);
		write(1, &hex[h % 16], 1);
	}
	else if (h < 16)
		write(1, &hex[h % 16], 1);
}
